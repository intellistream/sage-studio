⚠️  常见问题
━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━

Q: 模型下载很慢怎么办？
A: 设置镜像：export HF_ENDPOINT=https://hf-mirror.com

Q: 提示内存不足（OOM）？
A: 切换到 0.5B 模型，关闭其他应用

Q: 推理速度太慢？
A: 这是正常的，CPU 推理约为 GPU 的 1/10-1/50 速度
   建议：使用 0.5B 模型 + max_tokens=256

Q: 端口 8901 被占用？
A: 使用其他端口：--port 8902
   然后在 Studio 中选择对应的模型地址

