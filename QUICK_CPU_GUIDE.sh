✨ 优化推理速度：

   • 使用最小模型（0.5B）
   • 减少生成长度：max_tokens=256-512
   • 降低随机性：temperature=0.3-0.5
   • 关闭思考输出：enable_thinking=false

🎨 在 Pipeline 中使用：

   1. 拖拽 "Generator" 节点到画布
   2. 配置参数：
      - model_name: Qwen/Qwen2.5-0.5B-Instruct
      - api_base: http://localhost:8901/v1
      - max_tokens: 512
      - temperature: 0.5
   3. 连接其他节点（Retriever, Promptor 等）
   4. 点击 "运行" 执行

