━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━

步骤 1: 启动 CPU 模型
──────────────────────────────────────────────────

   方法 A - 使用一键脚本（推荐新手）：
   
      cd sage-studio
      ./start_cpu_model.sh
      
      # 然后选择：[1] Qwen2.5-0.5B（最快）
   
   方法 B - 直接命令（熟悉用户）：
   
      sage llm engine start Qwen/Qwen2.5-0.5B-Instruct \\
          --engine-kind llm --port 8901
   
   ⏱️  首次运行会下载模型（约 2-5 分钟）
   
步骤 2: 刷新 Studio 页面
──────────────────────────────────────────────────

   🌐 浏览器访问: http://localhost:5173
   
   刷新页面 (按 F5 或 Cmd+R)
   
步骤 3: 选择模型
──────────────────────────────────────────────────

   1. 点击右上角的模型选择器
   2. 找到 "Qwen/Qwen2.5-0.5B-Instruct"
   3. 点击选择
   4. 等待状态指示灯变绿 🟢
   5. 开始使用！

━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
